public class C_logical{
    public static void main(String args[]){

         boolean a = true; 
         boolean b = false;

         System.out.println(a && b) ;
         System.out.println(a || b); 
         System.out.println(!b); 


        System.out.println((3>2) && (5>0)); 

        System.out.println((3< 2) && (5>0));
         
        System.out.println((3>2) || (5>0)); 
    }
}